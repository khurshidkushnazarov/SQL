--Осон (Easy)
  

--Маълумот (Data)

--Маълумот — бу реал дунёдаги объектлар, ҳодисалар ёки жараёнлар ҳақидаги фактлар, рақамлар, белгилар, матнлар ёки бошқа турдаги ахборотлардир. Маълумот маълум бир контекстда тушунча ҳосил қилиш учун фойдаланилади.
--Мисол: "Аҳмад 25 ёшда" — бу маълумот.

--Маълумотлар базаси (Database)

--Маълумотлар базаси — бу маълумотларни тўплаш, сақлаш, бошқариш ва қайта ишлаш учун мўлжалланган тузилган маълумотлар мажмуасидир. У маълумотларни тизимли сақлаш ва уларга қулай тарзда мурожаат қилиш имконини беради.
--Мисол: Талабалар ҳақидаги маълумотлар сақланадиган электрон базалар.  

--Реляцион маълумотлар базаси (Relational Database)

--Реляцион маълумотлар базаси — бу маълумотлар жадвал кўринишида сақланадиган ва уларнинг ўзаро боғлиқлиги (реляцияси) орқали ташкил этилган маълумотлар базасидир. Ҳар бир жадвалда сатрлар ва устунлар мавжуд бўлади.
--Мисол: Microsoft SQL Server, MySQL, Oracle Database — реляцион МБМлардир.

--Жадвал (Table)

--Жадвал — реляцион маълумотлар базасида маълумотлар сақланадиган асосий структура. Жадвал сатрлар (row) ва устунлардан (column) ташкил топган бўлади. Ҳар бир сатр — маълум бир объект ёки ҳодисага оид маълумотларни, устунлар эса маълумот турини билдириш учун хизмат қилади.
--Мисол: TALABALAR жадвалида "ID", "Name", "Surname", "Age" каби устунлар бўлиши мумкин.


  --SQL Server  нинг бешта асосий хусусияти:

--Маълумотларни самарали бошқариш – SQL Server маълумотларни сақлаш, янгилаш, ўчириш ва қидириш учун кучли воситаларни тақдим этади.
--Хавфсизлик (Security) – Рўйхатдан ўтиш (authentication) ва рухсатлар (authorization) орқали маълумотларга рухсатсиз киришни чеклаш имконини беради.
--Захира нусха ва тиклаш (Backup & Restore) – Маълумотларни сақлаш ва тиклаш учун қулай механизмларга эга.
--Транзакцияларни қўллаб-қувватлаш (ACID қоидалари) – Маълумотлар яхлитлигини таъминлайдиган транзакция механизмларини таклиф қилади.
--Интеграция воситалари (Integration Tools) – SSIS (SQL Server Integration Services), SSRS (Reporting Services), SSAS (Analysis Services) каби қўшимча воситалар орқали маълумотларни таҳлил қилиш ва ҳисоботлар тайёрлаш имконияти мавжуд.



--Ўртача (Medium)  

CREATE DATABASE SchoolDB;
USE SchoolDB;

CREATE TABLE Students (
    StudentID INT PRIMARY KEY,
    Name VARCHAR(50),
    Age INT
);

-------SQL Server’га уланишда мавжуд бўлган турли аутентификация усулларини сананг (камида иккита):
--SQL (Structured Query Language)	Маълумотлар базасида маълумотларни яратиш, ўқиш, янгилаш ва ўчириш учун ишлатиладиган тил (яъни дастурлаш тили эмас, балки сўров тили).
--SQL Server	Microsoft томонидан ишлаб чиқилган реляцион маълумотлар базаси бошқарув тизими (RDBMS). Бу дастур маълумотларни сақлаш, бошқариш ва қайта ишлаш учун хизмат қилади.
--SSMS (SQL Server Management Studio)	SQL Server билан ишлаш учун мўлжалланган график интерфейсга эга дастур. У орқали сўровларни ёзиш, жадваллар яратиш, маълумотларни кўриш ва таҳлил қилиш мумкин.


SQL буйруқлари бир нечта турларга бўлинади. Қуйида асосий 5 тоифа:

--✅ DQL — Data Query Language

--Мақсад: Маълумотларни қидириш (сўров қилиш).
--Асосий буйруқ: SELECT
--Мисол:

SELECT * FROM Students;

  
--✅ DML — Data Manipulation Language

--Мақсад: Маълумотлар билан ишлаш — қўшиш, ўзгартириш, ўчириш.
--Асосий буйруқлар: INSERT, UPDATE, DELETE
--Мисоллар:

INSERT INTO Students (StudentID, Name, Age)
VALUES (1, 'Ali', 20);
UPDATE Students
SET Age = 21
WHERE StudentID = 1;
DELETE FROM Students
WHERE StudentID = 1;


--✅ DDL — Data Definition Language

--Мақсад: Жадвал ва структураларни яратиш, ўзгартириш, ўчириш.
--Асосий буйруқлар: CREATE, ALTER, DROP
--Мисоллар:

CREATE TABLE Courses (
    CourseID INT PRIMARY KEY,
    CourseName VARCHAR(100)
);
ALTER TABLE Students
ADD Gender VARCHAR(10);
DROP TABLE Courses;  


